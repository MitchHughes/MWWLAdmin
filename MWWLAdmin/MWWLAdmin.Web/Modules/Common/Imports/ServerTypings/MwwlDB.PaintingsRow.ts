namespace MWWLAdmin.MwwlDB {
    export interface PaintingsRow {
        Id?: string;
        Title?: string;
        CategoryId?: number;
        OriginalFileName?: string;
        Dimension?: string;
        Workshop?: boolean;
        Large?: boolean;
        Small?: boolean;
        OriginalAvailable?: boolean;
        OriginalPrice?: number;
        Framed?: boolean;
        CategoryName?: string;
        CategoryFolderName?: string;
    }

    export namespace PaintingsRow {
        export const idProperty = 'Id';
        export const nameProperty = 'Title';
        export const localTextPrefix = 'MwwlDB.Paintings';

        export namespace Fields {
            export declare const Id: string;
            export declare const Title: string;
            export declare const CategoryId: string;
            export declare const OriginalFileName: string;
            export declare const Dimension: string;
            export declare const Workshop: string;
            export declare const Large: string;
            export declare const Small: string;
            export declare const OriginalAvailable: string;
            export declare const OriginalPrice: string;
            export declare const Framed: string;
            export declare const CategoryName: string;
            export declare const CategoryFolderName: string;
        }

        ['Id', 'Title', 'CategoryId', 'OriginalFileName', 'Dimension', 'Workshop', 'Large', 'Small', 'OriginalAvailable', 'OriginalPrice', 'Framed', 'CategoryName', 'CategoryFolderName'].forEach(x => (<any>Fields)[x] = x);
    }
}

